# --- Что считается ПОКРЫТИЕМ сигнала ----------------------------------------
# Наивное «есть сделка по паре в окне» здесь не работает: самый активный пул
# (USDC/WETH) содержит сделку в 77% ВСЕХ блоков, поэтому при окне ±2 покрытым
# оказался бы почти каждый сигнал — метрика ничего не различала бы. Поэтому:
#
#   1) покрывающей считается только MEV-транзакция (bribe > 0; таких 12% от всех);
#   2) если у сигнала ≥2 DEX-ног/хопов — требуем АТОМАРНОСТЬ: одна транзакция
#      должна задеть минимум SIGNAL_MIN_ATOMIC_LEGS разных (нога, хоп) сигнала.
#      Настоящий арбитражник исполняет ноги одной транзакцией, случайный шум нет;
#   3) у сигналов с единственной DEX-ногой (42% выборки) атомарность проверить
#      не на чем — там фолбэк по объёму: объём сделки должен укладываться в
#      коридор [1/k, k] от объёма ноги, k = SIGNAL_VOLUME_TOLERANCE.
#
# Какой из критериев сработал, видно в колонке coverage_kind (atomic / volume).
SIGNAL_MIN_ATOMIC_LEGS = 2
SIGNAL_VOLUME_TOLERANCE = 3.0

