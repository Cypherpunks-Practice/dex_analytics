# --- Страница «Сигналы» -------------------------------------------------------
# Сколько последних сигналов тянуть из Postgres за один запрос.
SIGNALS_LIMIT = 2000
# Объём выборки сигналов теперь определяет ВЫБРАННАЯ ДАТА (min/max_timestamp в
# запросе), а не лимит. Этот большой лимит — лишь защита от OOM на «Всё время»:
# берём не больше стольких самых свежих сигналов в окне (см. signals_service).
SIGNALS_QUERY_LIMIT = 50000
# Окно блоков покрытия: сигнал считается ПОКРЫТЫМ, если есть хотя бы одна сделка
# по его паре в пределах ±SIGNAL_BLOCK_WINDOW блоков от found_block сигнала.
# Значение задаёт пользователь на фронтенде; здесь — дефолт до прихода ввода.
SIGNAL_BLOCK_WINDOW = 2
# Коридор допуска по объёму хопов покрывающей цепочки. Цепочка засчитывается
# покрытием, только если max(объём хопа)/min(объём хопа) <= этого значения
# (объёмы — ПОСЛЕ агрегации сплитов внутри хопа): через настоящий мультихоп течёт
# один и тот же капитал, поэтому хопы должны быть одного порядка объёма. Для
# одиночного хопа (прямая целевая пара / single-hop сигнал) проверка не применяется.
SIGNAL_HOP_VOLUME_TOLERANCE = 2.0
# Прогрессивная загрузка сигналов: целевой бюджет РАЗНЫХ блоков на один батч.
# Сигналы грузятся и дорисовываются на странице батчами; размер батча в сигналах =
# SIGNALS_BATCH_BLOCKS // (2*block_window+1). Так пик памяти на батч ограничен
# ~(бюджет блоков × плотность сделок), НЕЗАВИСИМО от ширины окна покрытия.
SIGNALS_BATCH_BLOCKS = 1500
# Клиентский фильтр «Дата» на странице сигналов: ключ TIME_RANGES → ширина окна
# от максимальной метки времени в загруженных данных (анкер по данным, как
# TIME_ANCHOR="data"). Ключа "all" нет — это «без ограничения». "yesterday" на
# клиенте трактуем как окно «последние 2 дня», а не фиксированные сутки.
SIGNALS_TIME_WINDOWS = {
    "last_hour": timedelta(hours=1),
    "today": timedelta(days=1),
    "yesterday": timedelta(days=2),
    "week": timedelta(weeks=1),
    "month": timedelta(days=30),
}

# --- Фильтры игроков: три отдельных поля в сайдбаре --------------------------
# Поле «Включить» режима НЕ имеет: его адреса всегда трактуются как include —
# показывать аналитику ПУЛОВ, в которых были эти игроки (вся активность пула +
# отдельно объём самих игроков в обогащённой таблице Топ-50 / микро / «ушли-зашли»).
#
# Исключение разнесено на два независимых поля без тоггла (оба — глобальное
# вычитание из всей аналитики, применяются одновременно):
#   «Исключить пулы игроков»   — убрать целиком пулы, в которых были эти адреса;
#   «Исключить сделки игроков» — убрать только сами сделки этих адресов
#                                (пулы остаются).

# --- Режим фильтра пулов (тоггл рядом с полем адресов пулов) -----------------
#   include — показывать только выбранные пулы;
#   exclude — показывать все пулы, КРОМЕ выбранных.
POOL_MODES = {
    "include": "Только",
    "exclude": "Кроме",
}
